void MakeNormal(const std::vector<Vertex3D>& vertices,
    std::vector<Vertex3D>& normalVertices,
    std::vector<uint16_t>& normalIndices)
{
    for (size_t i = 0; i < vertices.size(); i++)
    {
        Vertex3D vertex = vertices[i];

        vertex.texcoord.x = 0.0f;
        normalVertices.push_back(vertex);
        normalIndices.push_back(uint16_t(i * 2));

        vertex.texcoord.x = 1.0f;
        normalVertices.push_back(vertex);
        normalIndices.push_back(uint16_t(i * 2 + 1));
    }
}

void Fuck(std::vector<Vertex3D>& normalVertices,
    std::vector<uint16_t>& normalIndices)
{
    Vertex3D vertex;
    vertex.position = { 1.0f, 1.0f, -1.0f };
    vertex.normal = { 0.0f, 0.0f, -1.0f };
    vertex.texcoord = { 0.0f, 0.0f };

    normalVertices.push_back(vertex);

    vertex.texcoord = { 1.0f, 0.0f };
    normalVertices.push_back(vertex);

    normalIndices.push_back(0);
    normalIndices.push_back(1);
}

